# Controllability and Observability
"""

# ╔═╡ fc2454f0-5c6f-40ab-9720-04559dc447f2
md"""
The ability to design the eigenvalues of the closed-loop system with the choice
of K relies on the system in (8.10a) being controllable. The controllability of a
linear system is determined entirely by the column space of the controllabilitymatrix

$$\mathcal{C} = \begin{bmatrix} B & AB & A^2B & \cdots & A^{n-1}B \end{bmatrix}$$

If the matrix $\mathcal{C}$ has n linearly independent columns, so that it spans all of $\mathbb{R}^n$,
then the system in  is controllable.
"""

# ╔═╡ 615e2742-15f2-4f23-b20f-1d97ff8da25a
md"""
## Example
The notion of controllability is more easily understood by investigating a few simple examples. First, consider the following system:

$$\frac{d}{dt}\begin{bmatrix}x_1\\x_2\end{bmatrix}=\begin{bmatrix}1 &0\\0 &2\end{bmatrix}\begin{bmatrix}x_1\\x_2\end{bmatrix}+\begin{bmatrix}0\\1\end{bmatrix}u\implies\mathcal{C}=\begin{bmatrix}0 &0\\1 &2\end{bmatrix}$$
This system is not controllable because the controllablity matrix consists of two linearly dependent vectors. Even without checking the rank of the matrix it is easy to see that the system is not controllable.
"""

# ╔═╡ 9f867bcd-dda7-4540-99ea-fb28bf1ccf7b
begin 
	A = [1 0;0 2]
	B = [0 ;1]
	rank(ctrb(A,B))
end

# ╔═╡ d47f8901-98b3-4033-afa6-21b826ce1e0b
md"""
Modifying this example to include two actuation inputs makes the system
controllable by increasing the control authority:

$$\frac{d}{dt}\begin{bmatrix}x_1\\x_2\end{bmatrix}=\begin{bmatrix}1 &0\\0 &2\end{bmatrix}\begin{bmatrix}x_1\\x_2\end{bmatrix}+\begin{bmatrix}1&0\\0&1\end{bmatrix}\begin{bmatrix}u_1\\u_2\end{bmatrix}\implies\mathcal{C}=\begin{bmatrix}1&0&2&0\\0&1&0&1\end{bmatrix}$$
As you can see below the controllability matrix now has rank 2
"""

# ╔═╡ 74d75d40-b3ec-4bd0-bbd3-42c12de9177a
begin 
	A₂ = [1 0;0 2]
	B₂ = [1 0;0 1]
	rank(ctrb(A₂,B₂))
end

# ╔═╡ 72830bdc-75d0-4645-8506-e88cab6558d6
md"""
This fully actuated system is clearly controllable because $x_1$ and $x_2$ may be independently controlled with $u_1$ and $u_2$.
The most interesting cases are less obvious than these two examples. Consider the system.

$$\frac{d}{dt}\begin{bmatrix}x_1\\x_2\end{bmatrix}=\begin{bmatrix}1 &1\\0 &2\end{bmatrix}\begin{bmatrix}x_1\\x_2\end{bmatrix}+\begin{bmatrix}0\\1\end{bmatrix}u\implies\mathcal{C}=\begin{bmatrix}0 &1\\1 &2\end{bmatrix}$$
This system is now controllable through the couples dynamics.
"""

